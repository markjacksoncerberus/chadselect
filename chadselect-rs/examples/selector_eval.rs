//! Real-world selector performance evaluation suite.
//!
//! ## Why this exists
//!
//! After the v0.3.x XPath overhaul (sxd → chadpath, our xrust fork over the
//! shared `scraper`/html5ever DOM), production **memory** dropped as intended
//! but fleet **CPU** still pegs at ~100%. The unit tests and the `page(n)`
//! micro-benches look fine because they exercise a handful of hand-written
//! selectors. The crawler fleet runs ~2,000 *real* selectors — and some
//! structural shapes the micro-benches never hit are pathological.
//!
//! This suite closes that gap by replaying the **actual** selectors the
//! `hermes_rust` fleet feeds chadselect against one giant synthetic page, and
//! ranking them by warm per-evaluation CPU cost. The selectors don't need to
//! return real data — the cost is in *walking* the document, and `//`-rooted
//! XPath walks the whole tree regardless of whether it matches anything.
//!
//! ## Data inputs (regenerate with `python3 eval/extract_selectors.py`)
//!   * `eval/selectors.json`     — every unique css:/xpath: selector pulled
//!                                  from `hermes_rust/src/crawlers/*.rs`
//!                                  (string-literal lexing + `>>` pipe stripped).
//!   * `eval/corpus_vocab.json`  — the tag / class-token / data-attr / id /
//!                                  label-text vocabulary mined from those
//!                                  selectors, so the synthetic HTML "speaks
//!                                  the same language" and selectors actually
//!                                  match nodes (which is what triggers the
//!                                  O(n²) predicate / following-sibling blowups).
//!
//! ## What it measures
//!   1. **Warm eval** — doc parsed once, each selector run via the public
//!      `ChadSelect::query(-1, ..)` path (all matches), best-of-N per-eval time.
//!      This is the steady-state per-selector CPU the fleet pays per page.
//!   2. **Scaling probe** — the top-K slowest selectors are re-timed at 1×, 2×,
//!      4× document size. The fitted exponent (`log2(t4/t1)/2`) separates O(n)
//!      (~1.0) from the O(n²) (~2.0) shapes that actually peg the CPU.
//!
//! ## Run
//! ```text
//! cargo run --release --example selector_eval                # default size
//! cargo run --release --example selector_eval -- --blocks 800 --top 40
//! cargo run --release --example selector_eval -- --engine xpath --csv eval/results.csv
//! ```

use std::collections::BTreeMap;
use std::time::{Duration, Instant};

use chadselect::ChadSelect;
use serde_json::Value;

// ─────────────────────────────────────────────────────────────────────────────
//  Inputs (generated by eval/extract_selectors.py)
// ─────────────────────────────────────────────────────────────────────────────

const SELECTORS_JSON: &str = include_str!("../eval/selectors.json");
const VOCAB_JSON: &str = include_str!("../eval/corpus_vocab.json");

#[derive(Clone)]
struct Selector {
    engine: String,  // "css" | "xpath"
    selector: String,
    sites: usize, // how many crawler sites use it (popularity weight)
}

struct Vocab {
    tags: Vec<String>,
    class_tokens: Vec<String>,
    data_attrs: Vec<String>,
    ids: Vec<String>,
    label_texts: Vec<String>,
}

fn load_selectors(engine_filter: Option<&str>) -> Vec<Selector> {
    let v: Value = serde_json::from_str(SELECTORS_JSON).expect("selectors.json parse");
    v.as_array()
        .unwrap()
        .iter()
        .filter_map(|r| {
            let engine = r["engine"].as_str()?.to_string();
            if engine != "css" && engine != "xpath" {
                return None; // this suite ranks DOM selectors only
            }
            if let Some(f) = engine_filter {
                if engine != f {
                    return None;
                }
            }
            Some(Selector {
                engine,
                selector: r["selector"].as_str()?.to_string(),
                sites: r["count"].as_u64().unwrap_or(1) as usize,
            })
        })
        .collect()
}

fn load_vocab() -> Vocab {
    let v: Value = serde_json::from_str(VOCAB_JSON).expect("corpus_vocab.json parse");
    let arr = |k: &str| {
        v[k].as_array()
            .map(|a| a.iter().filter_map(|x| x.as_str().map(String::from)).collect())
            .unwrap_or_default()
    };
    Vocab {
        tags: arr("tags"),
        class_tokens: arr("class_tokens"),
        data_attrs: arr("data_attrs"),
        ids: arr("ids"),
        label_texts: arr("label_texts"),
    }
}

// ─────────────────────────────────────────────────────────────────────────────
//  Giant synthetic HTML corpus
// ─────────────────────────────────────────────────────────────────────────────

/// Build a large dealer-style page from `blocks` vehicle cards.
///
/// Each card mixes the structures the real selectors target — label/value
/// sibling pairs (`<span class="info__label">Mileage</span><span
/// class="value">…</span>`), spec tables, definition lists, deeply nested
/// wrapper divs, data-attributes, ids and ld+json — using the *mined*
/// vocabulary so `contains(@class,…)`, `text()='…'` and `following-sibling::`
/// predicates land on real nodes. The deep wrappers + many siblings are what
/// make `//a//b`, `//x/following-sibling::y` and nested-predicate selectors
/// pay O(n²).
fn build_corpus(blocks: usize, v: &Vocab) -> String {
    let mut s = String::with_capacity(blocks * 4096 + 1024);
    s.push_str("<!doctype html><html><head><title>Inventory</title></head><body>");
    // outer wrappers carrying common ids → deepens every `//`-rooted walk
    s.push_str("<div id=\"vehicleInfo\"><div id=\"specifications\"><main class=\"content\">");
    s.push_str("<div id=\"fixed-info-container\" class=\"row inventoryDataPoints\">");

    let tag = |i: usize| v.tags.get(i % v.tags.len().max(1)).map(String::as_str).unwrap_or("div");
    let cls = |i: usize| {
        v.class_tokens
            .get(i % v.class_tokens.len().max(1))
            .map(String::as_str)
            .unwrap_or("spec")
    };

    for b in 0..blocks {
        let id = v.ids.get(b % v.ids.len().max(1)).map(String::as_str).unwrap_or("vehicle");
        let da = v
            .data_attrs
            .get(b % v.data_attrs.len().max(1))
            .map(String::as_str)
            .unwrap_or("data-vin");
        // card root: two class tokens + a data-attr so attribute & class
        // predicates both bite.
        s.push_str(&format!(
            "<div id=\"{id}-{b}\" class=\"vehicle {c1} {c2} list-group-item\" {da}=\"VIN{b:06}\" data-vin=\"1HGCM{b:06}\" data-name=\"Vehicle {b}\">",
            c1 = cls(b),
            c2 = cls(b + 7),
        ));

        // ── label/value sibling list (info__item / info__label / value) ──
        // following-sibling::*[…] selectors key off these.
        s.push_str("<ul class=\"specifications spec-info\">");
        for (k, label) in v.label_texts.iter().enumerate() {
            s.push_str(&format!(
                "<li class=\"info__item info__item--{k} basic-info-item\">\
                   <span class=\"info__label specifics-label dws-vehicle-fields-label\">{label}</span>\
                   <span class=\"value specifics-value feature-value\">{label} {b}</span>\
                 </li>",
            ));
        }
        s.push_str("</ul>");

        // ── definition list (features_snapshot__label / __detail) ──
        s.push_str("<dl class=\"features_snapshot vehicle-summary-item\">");
        for label in v.label_texts.iter().take(10) {
            s.push_str(&format!(
                "<dt class=\"features_snapshot__label vehicle-label\">{label}</dt>\
                 <dd class=\"features_snapshot__detail vehicle-summary-item-value\">{label} v{b}</dd>",
            ));
        }
        s.push_str("</dl>");

        // ── spec table (td label / td value) ──
        s.push_str("<table class=\"specifications af-quick-spec\"><tbody>");
        for label in v.label_texts.iter().take(12) {
            s.push_str(&format!(
                "<tr class=\"spec\"><td class=\"specifics-label af-quick-spec-label DetailsSpecsLabel\">{label}</td>\
                 <td class=\"specifics-value feature-value\">{label} {b}</td></tr>",
            ));
        }
        s.push_str("</tbody></table>");

        // ── deep wrapper chain — adds tree DEPTH so descendant walks are
        //    expensive; ends in <p class="optInteriorColor"> style nodes the
        //    concat()/substring-after() selectors target. ──
        let depth = 8;
        for d in 0..depth {
            s.push_str(&format!("<{t} class=\"{c} detail-info box-dataPoints\">", t = tag(d), c = cls(b + d)));
        }
        s.push_str(
            "<p class=\"optInteriorColor _optInteriorColor af-spec\">Interior Color: Tan</p>\
             <p class=\"optInterior\">Exterior Color: Black</p>",
        );
        for d in 0..depth {
            s.push_str(&format!("</{t}>", t = tag(depth - 1 - d)));
        }

        // ── misc leaf nodes selectors commonly grab ──
        s.push_str(&format!(
            "<a class=\"title pill\" href=\"tel:555{b:07}\">Call</a>\
             <a href=\"mailto:s{b}@x.com\">Email</a>\
             <img class=\"similar-vehicles\" data-src=\"/img/{b}.jpg\" src=\"/img/{b}.jpg\">\
             <input type=\"hidden\" name=\"vin\" value=\"1HGCM{b:06}\">\
             <meta itemprop=\"price\" content=\"{p}\">",
            p = 10000 + b,
        ));
        s.push_str(&format!(
            "<script type=\"application/ld+json\">{{\"@type\":\"Vehicle\",\"vehicleIdentificationNumber\":\"1HGCM{b:06}\",\"offers\":{{\"price\":{p}}}}}</script>",
            p = 10000 + b,
        ));

        s.push_str("</div>"); // card
    }

    s.push_str("</div></main></div></div></body></html>");
    s
}

// ─────────────────────────────────────────────────────────────────────────────
//  Timing
// ─────────────────────────────────────────────────────────────────────────────

struct Timing {
    per_eval: Duration,
    matches: usize,
    iters: u32,
}

/// Warm-time one selector against an already-parsed `ChadSelect`. Adaptive:
/// runs until either `budget` elapses or `max_iters` reached; if a single eval
/// already blows the budget (the pathological case), it records that one shot.
fn time_selector(cs: &ChadSelect, prefixed: &str, budget: Duration, max_iters: u32) -> Timing {
    // First eval also tells us the match count and whether it's a slow one.
    let t0 = Instant::now();
    let first = cs.query(-1, prefixed);
    let first_elapsed = t0.elapsed();
    let matches = first.len();

    if first_elapsed >= budget || max_iters <= 1 {
        return Timing { per_eval: first_elapsed, matches, iters: 1 };
    }

    let mut iters: u32 = 1;
    let loop_start = Instant::now();
    while loop_start.elapsed() < budget && iters < max_iters {
        let r = cs.query(-1, prefixed);
        std::hint::black_box(&r);
        iters += 1;
    }
    let total = loop_start.elapsed() + first_elapsed;
    Timing { per_eval: total / iters, matches, iters }
}

// ─────────────────────────────────────────────────────────────────────────────
//  Structural feature tagging (for aggregate diagnosis)
// ─────────────────────────────────────────────────────────────────────────────

fn features(sel: &Selector) -> Vec<&'static str> {
    let s = &sel.selector;
    let mut f = Vec::new();
    if sel.engine == "xpath" {
        if s.matches("//").count() >= 2 {
            f.push("chained // (descendant×2+)");
        } else if s.contains("//") {
            f.push("rooted // descendant");
        }
        if s.contains("following-sibling") || s.contains("preceding-sibling") {
            f.push("sibling axis");
        }
        if s.contains("following::") || s.contains("preceding::") {
            f.push("following/preceding axis");
        }
        if s.contains("ancestor") {
            f.push("ancestor axis");
        }
        if s.contains("contains(") {
            f.push("contains()");
        }
        if s.matches('[').count() >= 2 {
            f.push("multi-predicate");
        }
        if s.contains("normalize-space(") || s.contains("concat(") || s.contains("substring") {
            f.push("string-fn heavy");
        }
        if s.contains("text()") {
            f.push("text()");
        }
    } else {
        if s.contains('*') {
            f.push("css attr-substring *=");
        }
        if s.matches(' ').count() >= 2 {
            f.push("css deep descendant");
        }
        if s.contains(">>") {
            f.push("css pipe");
        }
    }
    if f.is_empty() {
        f.push("simple");
    }
    f
}

fn fmt(d: Duration) -> String {
    let us = d.as_secs_f64() * 1e6;
    if us >= 1000.0 {
        format!("{:>9.3} ms", us / 1000.0)
    } else {
        format!("{:>9.1} µs", us)
    }
}

// ─────────────────────────────────────────────────────────────────────────────
//  Main
// ─────────────────────────────────────────────────────────────────────────────

fn main() {
    // ── args ──
    let mut blocks = 400usize;
    let mut top = 30usize;
    let mut engine_filter: Option<String> = None;
    let mut csv_path: Option<String> = None;
    let mut budget_ms = 25u64;
    let mut probe = true;
    let mut args = std::env::args().skip(1);
    while let Some(a) = args.next() {
        match a.as_str() {
            "--blocks" => blocks = args.next().and_then(|s| s.parse().ok()).unwrap_or(blocks),
            "--top" => top = args.next().and_then(|s| s.parse().ok()).unwrap_or(top),
            "--engine" => engine_filter = args.next(),
            "--csv" => csv_path = args.next(),
            "--budget-ms" => budget_ms = args.next().and_then(|s| s.parse().ok()).unwrap_or(budget_ms),
            "--no-probe" => probe = false,
            other => eprintln!("warning: ignoring unknown arg {other}"),
        }
    }
    let budget = Duration::from_millis(budget_ms);

    let vocab = load_vocab();
    let selectors = load_selectors(engine_filter.as_deref());

    eprintln!("── building corpus ({blocks} vehicle cards) ──");
    let html = build_corpus(blocks, &vocab);
    let n_css = selectors.iter().filter(|s| s.engine == "css").count();
    let n_xp = selectors.iter().filter(|s| s.engine == "xpath").count();

    // Parse once (warm). Trigger the lazy parse with a trivial query and time it
    // so we can report the per-page parse cost the fleet pays cold.
    let mut cs = ChadSelect::new();
    cs.add_html(html.clone());
    let tp = Instant::now();
    let _ = cs.query(-1, "css:html");
    let parse_cost = tp.elapsed();

    println!("\n╔══════════════════════════════════════════════════════════════════════════╗");
    println!("║  chadselect real-selector performance suite                                ║");
    println!("╚══════════════════════════════════════════════════════════════════════════╝");
    println!(
        "corpus     : {} bytes, {} vehicle cards",
        html.len(),
        blocks
    );
    println!("parse (1×) : {} (html5ever, paid once per page, cold)", fmt(parse_cost));
    println!("selectors  : {} css + {} xpath = {} total", n_css, n_xp, selectors.len());
    println!("warm budget: {} ms per selector (best-effort repeats)\n", budget_ms);

    // ── warm-time every selector ──
    let mut rows: Vec<(Selector, Timing)> = Vec::with_capacity(selectors.len());
    let suite_start = Instant::now();
    for (i, sel) in selectors.iter().enumerate() {
        let prefixed = format!("{}:{}", sel.engine, sel.selector);
        let t = time_selector(&cs, &prefixed, budget, 200);
        rows.push((sel.clone(), t));
        if (i + 1) % 200 == 0 {
            eprintln!("  timed {}/{} …", i + 1, selectors.len());
        }
    }
    let suite_elapsed = suite_start.elapsed();

    rows.sort_by(|a, b| b.1.per_eval.cmp(&a.1.per_eval));

    // ── slowest selectors ──
    println!("──────────────────────────────────────────────────────────────────────────");
    println!(" TOP {} SLOWEST SELECTORS (warm per-eval CPU)", top);
    println!("──────────────────────────────────────────────────────────────────────────");
    println!(" {:>12}  {:>6}  {:>5}  {:<6} selector", "per-eval", "match", "sites", "eng");
    for (sel, t) in rows.iter().take(top) {
        let mut disp = sel.selector.clone();
        if disp.len() > 88 {
            disp.truncate(85);
            disp.push_str("...");
        }
        println!(
            " {:>12}  {:>6}  {:>5}  {:<6} {}",
            fmt(t.per_eval),
            t.matches,
            sel.sites,
            sel.engine,
            disp
        );
    }

    // ── aggregate by structural feature ──
    let mut feat_total: BTreeMap<&'static str, (Duration, usize)> = BTreeMap::new();
    for (sel, t) in &rows {
        for f in features(sel) {
            let e = feat_total.entry(f).or_insert((Duration::ZERO, 0));
            e.0 += t.per_eval;
            e.1 += 1;
        }
    }
    let mut feat_rows: Vec<_> = feat_total.into_iter().collect();
    feat_rows.sort_by(|a, b| (b.1 .0 / b.1 .1 as u32).cmp(&(a.1 .0 / a.1 .1 as u32)));
    println!("\n──────────────────────────────────────────────────────────────────────────");
    println!(" COST BY STRUCTURAL FEATURE (mean warm per-eval, selectors carrying it)");
    println!("──────────────────────────────────────────────────────────────────────────");
    println!(" {:>12}  {:>5}  feature", "mean", "count");
    for (f, (sum, n)) in &feat_rows {
        println!(" {:>12}  {:>5}  {}", fmt(*sum / *n as u32), n, f);
    }

    // ── scaling probe on the worst offenders: 1× / 2× / 4× ──
    let probe_n = if probe { top.min(rows.len()).min(25) } else { 0 };
    if probe {
    println!("\n──────────────────────────────────────────────────────────────────────────");
    println!(" SCALING PROBE — top {} offenders at 1× / 2× / 4× corpus size", probe_n);
    println!(" exponent ≈ log2(t4/t1)/2 :  ~1.0 = linear,  ~2.0 = O(n²) blowup");
    println!("──────────────────────────────────────────────────────────────────────────");
    let html2 = build_corpus(blocks * 2, &vocab);
    let html4 = build_corpus(blocks * 4, &vocab);
    let mut cs2 = ChadSelect::new();
    cs2.add_html(html2);
    let _ = cs2.query(-1, "css:html");
    let mut cs4 = ChadSelect::new();
    cs4.add_html(html4);
    let _ = cs4.query(-1, "css:html");
    println!(
        " {:>11} {:>11} {:>11}  {:>5}  selector",
        "t1×", "t2×", "t4×", "exp"
    );
    for (sel, t1) in rows.iter().take(probe_n) {
        let prefixed = format!("{}:{}", sel.engine, sel.selector);
        let p2 = time_selector(&cs2, &prefixed, budget, 50);
        let p4 = time_selector(&cs4, &prefixed, budget, 50);
        let r = p4.per_eval.as_secs_f64() / t1.per_eval.as_secs_f64().max(1e-9);
        let exp = if r > 0.0 { r.log2() / 2.0 } else { 0.0 };
        let mut disp = sel.selector.clone();
        if disp.len() > 70 {
            disp.truncate(67);
            disp.push_str("...");
        }
        println!(
            " {:>11} {:>11} {:>11}  {:>5.2}  {}",
            fmt(t1.per_eval),
            fmt(p2.per_eval),
            fmt(p4.per_eval),
            exp,
            disp
        );
    }
    } // end probe

    println!(
        "\nsuite warm-timing pass: {} over {} selectors\n",
        fmt(suite_elapsed),
        selectors.len()
    );

    // ── optional CSV dump (full ranking) ──
    if let Some(path) = csv_path {
        let mut out = String::from("rank,engine,per_eval_us,matches,sites,iters,selector\n");
        for (i, (sel, t)) in rows.iter().enumerate() {
            let esc = sel.selector.replace('"', "\"\"");
            out.push_str(&format!(
                "{},{},{:.3},{},{},{},\"{}\"\n",
                i + 1,
                sel.engine,
                t.per_eval.as_secs_f64() * 1e6,
                t.matches,
                sel.sites,
                t.iters,
                esc
            ));
        }
        std::fs::write(&path, out).expect("write csv");
        eprintln!("wrote full ranking → {path}");
    }
}
